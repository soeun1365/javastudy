package quiz04;

public class Book {

	String title;
	String writter;
	int price;
	
	void set(String t, String w, int p) {
		title = t;
		writter = w;
		price = p;
	}

	void set(String t, String w) {
		set(t, w, 0);
	}
	void set(String t, int p) {
		set(t, "미상", p);
	}
	
	void info() {
		System.out.println("제목: " + title + ", 저자: " + writter + ", 가격: " + price);
		System.out.println();
	}

}
